export const mergeWithLocalPublishedArticles = (remoteArticles = [], options = {}) => {
  const bySlug = new Map();
  LOCAL_PUBLISHED_ARTICLES.forEach((item) => bySlug.set(item.slug, item));
  (Array.isArray(remoteArticles) ? remoteArticles : [])
    .filter((item) => item?.slug)
    .forEach((item) => bySlug.set(item.slug, item));

  const merged = Array.from(bySlug.values()).sort((a, b) => {
    const aDate = new Date(a?.updated_at || a?.published_at || a?.created_at || 0).getTime();
    const bDate = new Date(b?.updated_at || b?.published_at || b?.created_at || 0).getTime();
    return bDate - aDate;
  });

  if (typeof options.limit === 'number') return merged.slice(0, options.limit);
  return merged;
};
